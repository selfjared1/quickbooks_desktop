def convert_to_python(str_proper):
    str_proper.replace("Query", "")
    new_str = ''
    for i, letter in enumerate(str_proper):
        if i == 0:
            new_str += letter.lower()
        elif i and letter.isupper():
            new_str += f'_{letter.lower()}'
        else:
            new_str += letter
    return new_str

